'use strict'

import { app, BrowserWindow } from 'electron'
import '../renderer/store' // if you enabled createSharedMutations() plugin you need to create an instance of store in the main process 坑爹啊

/**
 * Set `__static` path to static files in production
 * https://simulatedgreg.gitbooks.io/electron-vue/content/en/using-static-assets.html
 */
if (process.env.NODE_ENV !== 'development') {
  global.__static = require('path')
    .join(__dirname, '/static')
    .replace(/\\/g, '\\\\')
}

let mainWindow
const isDev = process.env.NODE_ENV === 'development'
const winURL = isDev
  ? `http://localhost:9080`
  : `file://${__dirname}/index.html`

function createWindow() {
  /**
   * Initial window options
   */
  mainWindow = new BrowserWindow({
    height: 890,
    minHeight: 890,
    useContentSize: true,
    width: 500,
    minWidth: 500,
    webPreferences: { webSecurity: !isDev }
  })

  mainWindow.loadURL(winURL)
  mainWindow.setMenu(null)

  mainWindow.on('closed', () => {
    mainWindow = null
  })
}

function initApp() {
  createWindow()
  if (process.platform !== 'darwin') {
    try {
      isDev
        ? app.setPath('temp', `E:/${app.getName()}/temp`)
        : app.setPath('temp', `${__dirname}/temp`)
    } catch (e) {
      console.log(e)
    }
  }
}

app.on('ready', initApp)

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit()
  }
})

app.on('activate', () => {
  if (mainWindow === null) {
    createWindow()
  }
})

